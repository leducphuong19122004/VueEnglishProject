<p><strong>Lời</strong> <strong>nói</strong> <strong>gián</strong> <strong>tiếp</strong> <strong>--</strong> <strong>câu</strong> <strong>tường</strong> <strong>thuật</strong>
<strong>(indirect</strong> <strong>speech):</strong> Là thuật lại lời nói của một người khác trong
