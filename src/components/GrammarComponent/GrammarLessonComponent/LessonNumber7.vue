<p><strong>2.</strong> <strong>Cách</strong> <strong>chuyển</strong> <strong>từ</strong> <strong>câu</strong> <strong>trực</strong> <strong>tiếp</strong> <strong>sang</strong>
<strong>câu</strong> <strong>gián</strong> <strong>tiếp</strong></p>
<p><strong>Bước</strong> <strong>1:</strong> <strong>Xác</strong> <strong>định</strong> <strong>từ</strong> <strong>tường</strong> <strong>thuật</strong></p>
<p>Với <strong>câu</strong> <strong>tường</strong> <strong>thuật</strong>, chúng ta có 2 động từ:</p>
<blockquote>
<p>● Với <strong>told</strong>: Bắt buộc dùng khi chúng ta thuật lại</p>
<p>● Với <strong>said</strong>: Thuật lại khi không nhắc đến người thứ 3.</p>
</blockquote>
<p>Ngoài ra còn các động từ khác <strong>asked,</strong> <strong>denied,</strong> <strong>promised</strong>,
...tuy nhiên sẽ không sử dụng cấu trúc giống said that.</p>
<p><strong>Bước</strong> <strong>2:</strong> <strong>Lùi</strong> <strong>thì</strong> <strong>của</strong> <strong>động</strong> <strong>từ</strong> <strong>trong</strong>
<strong>câu</strong> <strong>tường</strong> <strong>thuật</strong> <strong>về</strong> <strong>quá</strong> <strong>khứ</strong></p>
<p>Với mệnh đề được tường thuật, ta hiểu rằng sự việc đó không xảy ra ở
thời điểm nói nữa mà thuật lại lời nói trong quá khứ. Do đó, động từ
trong câu sẽ được lùi về thì quá khứ 1 thì so với thời điểm nói. Tổng
quát như sau</p>
<blockquote>
<p>● Thì tương lai lùi về tương lai trong quá khứ ● Thì hiện tại lùi về
quá khứ</p>
<p>● Thì quá khứ lùi về quá khứ hoàn thành</p>
</blockquote>
<p><strong>Bước</strong> <strong>3:</strong> <strong>Đổi</strong> <strong>đại</strong> <strong>từ</strong> <strong>nhân</strong> <strong>xưng,</strong> <strong>tính</strong>
<strong>từ</strong> <strong>sở</strong> <strong>hữu</strong> <strong>và</strong> <strong>đại</strong> <strong>từ</strong> <strong>sở</strong> <strong>hữu</strong></p>
<p>Khi tường thuật lại câu nói của Nam, ta sẽ không thể nói là &quot;Bạn gái tôi
sẽ đến đây thăm tôi vào ngày mai&quot; mà tường thuật lại lời của Nam rằng
&quot;Bạn gái của Nam sẽ đến thăm anh ấy...&quot;. Tương ứng với nó, khi chuyển
đổi từ câu trực tiếp sang gián tiếp, ta cũng cần lưu ý thay đổi đại từ
nhân xưng, tính từ sở hữu và đại từ sở hữu trong mệnh đề được tường
thuật tương ứng.</p>
<p><strong>Bước</strong> <strong>4:</strong> <strong>Đổi</strong> <strong>cụm</strong> <strong>từ</strong> <strong>chỉ</strong> <strong>thời</strong> <strong>gian,</strong>
