<p>Ví dụ: Hoa said, &quot;I want to go home&quot;</p>
<p>-&gt;Hoa said she wanted to go home là câu gián tiếp (indirect speech)</p>
<p><strong>1.</strong> <strong>Phân</strong> <strong>biệt</strong> <strong>câu</strong> <strong>trực</strong> <strong>tiếp</strong> <strong>--</strong> <strong>câu</strong>
