<p><strong>2.</strong> <strong>To-Infinitive</strong></p>
<p><strong>2.1.</strong> <strong>Khái</strong> <strong>niệm</strong></p>
<p>Infinitives là hình thức động từ nguyên mẫu. Có 2 loại động từ nguyên
mẫu</p>
<blockquote>
<p>● Động từ nguyên mẫu có &quot;to&quot; (to infinitives)</p>
<p>● Động từ nguyên dạng không to (bare infinitives).</p>
</blockquote>
<p>Để thuận tiện, người Anh &#39;ngầm&#39; quy ước rằng khi nói &#39;động từ nguyên
mẫu&#39; (infinitives) có nghĩa là &#39;động từ nguyên mẫu có to&#39; còn khi muốn
nói &#39;động từ nguyên mẫu không to&#39; thì người ta phải nói đầy đủ
&#39;infinitives without to&#39;.</p>
<p>Eg:</p>
<blockquote>
<p>● Infinitive: to learn, to watch, to play...</p>
<p>● Bare infinitive: learn, watch, play...</p>
</blockquote>
<p>Phủ định của động từ nguyên mẫu được hình thành bằng cách thêm &quot;not&quot; vào
trước &quot;to V&quot; hoặc &quot;V&quot;.</p>
<p>Eg:</p>
<blockquote>
<p>● Infinitive: not to learn, not to watch, not to play...</p>
<p>● Bare infinitive: not learn, not watch, not play...</p>
</blockquote>
