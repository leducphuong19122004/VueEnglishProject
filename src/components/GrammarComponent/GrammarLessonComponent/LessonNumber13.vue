<p>20. <strong>/l/:</strong> Từ từ cong lưỡi, chạm vào răng hàm trên, thanh quản
rung, môi mở rộng, môi mở hoàn toàn, đầu lưỡi từ từ cong lên và đặt
vào răng hàm trên.</p>
<p>21. <strong>/r</strong> <strong>/:</strong> Khác /r/ tiếng Việt: Lưỡi cong vào trong và môi
tròn, hơi chu về phía trước. Khi luồng khí thoát ra, lưỡi về trạng
thái thả lỏng, môi tròn mở rộng.</p>
<p>22. <strong>/w/:</strong> Lưỡi thả lỏng, môi tròn và chu về trước. Khi luồng khí
thoát ra, lưỡi vẫn thả lỏng, môi tròn mở rộng.</p>
<p>23. <strong>/h/:</strong> Như âm /h/ tiếng Việt, không rung thanh quản, môi hé
nửa, lưỡi hạ thấp để luồng khí thoát ra.</p>
<p>24. <strong>/j/:</strong> Nâng phần trước của lưỡi lên gần ngạc cứng, đẩy luồng
khí thoát ra giữa phần trước của lưỡi và ngạc cứng nhưng không có
tiếng ma sát của luồng khí (do khoảng cách giữa phần trước của lưỡi và
ngạc cứng không quá gần) làm rung dây thanh</p>
<p>trong cổ họng. Môi hơi mở khi luồng khí thoát ra, môi mở rộng, phần
giữa lưỡi hơi nâng lên, khi luồng khí thoát ra, lưỡi thả lỏng.</p>
