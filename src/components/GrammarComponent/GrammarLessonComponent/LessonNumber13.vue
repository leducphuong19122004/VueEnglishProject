<p><strong>2.</strong> <strong>Phụ</strong> <strong>âm</strong> <strong>tiếng</strong> <strong>Anh</strong></p>
<p>Chúng ta có 24 phụ âm: <strong>/p/;</strong> <strong>/b/;</strong> <strong>/t/;</strong> <strong>/d/;</strong> <strong>/t∫/;</strong>
<strong>/dʒ/;</strong> <strong>/k/;</strong> <strong>/g/;</strong> <strong>/f/;</strong> <strong>/v/;</strong> <strong>/ð/;</strong> <strong>/θ/;</strong> <strong>/s/;</strong>
<strong>/z/;</strong> <strong>/∫/;</strong> <strong>/ʒ/;</strong> <strong>/m/;</strong> <strong>/n/;</strong> <strong>/η/;</strong> <strong>/l/;</strong> <strong>/r/;</strong>
<strong>/w/;</strong> <strong>/j/</strong></p>
<blockquote>
<p>1. <strong>/p/:</strong> Đọc gần giống với âm /p/ tiếng Việt, lực chặn của 2 môi
không mạnh bằng, nhưng hơi thoát ra vẫn mạnh như vậy. Hai môi chặn
luồng khí trong miệng, sau đó bật mạnh luồng khí ra.</p>
<p>2. <strong>/b/:</strong> Giống âm /b/ tiếng Việt. Hai môi chặn luồng khí trong
miệng, sau đó bật mạnh luồng khí ra.</p>
<p>3. <strong>/t/:</strong> Âm /t/ tiếng Việt, nhưng bật hơi thật mạnh, đặt đầu lưỡi
dưới nướu, khi luồng khí bật ra, đầu lưỡi chạm vào răng cửa dưới. Hai
răng khít chặt, mở ra khi luồng khí mạnh thoát ra.</p>
<p>4. <strong>/d/:</strong> Giống âm /d/ tiếng Việt nhưng hơi bật ra mạnh hơn một
chút. Đặt đầu lưỡi dưới nướu, khi luồng khí bật ra, đầu lưỡi chạm vào
răng cửa dưới, hai răng khít chặt, mở ra khi luồng khí mạnh thoát ra.</p>
<p>5. <strong>/t∫/:</strong> Giống âm /ch/ tiếng Việt nhưng môi khi nói phải chu ra.
Môi hơi tròn và chu về phía trước, khi luồng khí thoát ra, môi tròn
nửa, lưỡi thẳng và chạm vào hàm dưới, để khí thoát ra trên bề mặt
lưỡi.</p>
<p>6. <strong>/dʒ/:</strong> Giống âm /t∫/ nhưng có rung dây thanh quản. Môi hơi tròn
và chu về phía trước, khi luồng khí thoát ra, môi tròn nửa, lưỡi thẳng
và chạm vào hàm dưới, để khí thoát ra trên bề mặt lưỡi.</p>
<p>7. <strong>/k/:</strong> Giống âm /k/ tiếng Việt nhưng bật mạnh hơi, nâng phần sau
của lưỡi, chạm ngạc mềm, hạ thấp khi luồng khí mạnh bật ra.</p>
<p>8. <strong>/g/:</strong> Giống âm /g/ tiếng Việt, nâng phần sau của lưỡi, chạm ngạc
mềm, hạ thấp khi luồng khí mạnh bật ra.</p>
<p>9. <strong>/f/:</strong> Giống âm /ph/ (phở) trong tiếng Việt, hàm trên chạm nhẹ
vào môi dưới. 10. <strong>/v/:</strong> Giống âm /v/ trong tiếng Việt, hàm trên
chạm nhẹ vào môi dưới.</p>
<p>11. <strong>/ð/:</strong> Đặt đầu lưỡi ở giữa hai hàm răng, để luồng khí thoát ra
giữa lưỡi và hai hàm răng, thanh quản rung.</p>
<p>12. <strong>/θ/:</strong> Đặt đầu lưỡi ở giữa hai hàm răng, để luồng khí thoát ra
giữa lưỡi và hai hàm răng, thanh quản không rung.</p>
<p>13. <strong>/s/:</strong> Để lưỡi đặt nhẹ lên hàm trên, ngạc mềm nâng. Luồng khí
thoát từ giữa mặt lưỡi và lợi. Không rung thanh quản, để mặt lưỡi chạm
nhẹ vào lợi hàm trên.</p>
<p>14. <strong>/z/:</strong> Để lưỡi đặt nhẹ lên hàm trên, ngạc mềm nâng, luồng khí
thoát từ giữa mặt lưỡi và lợi, rung thanh quản.</p>
<p>15. <strong>/∫/</strong>: Môi chu ra (giống khi yêu cầu ng khác im lặng:
Shhhhhh!). Môi hướng về phía trước như đang kiss ai đó, môi tròn, để
mặt lưỡi chạm lợi hàm trên, nâng phần trước của lưỡi lên.</p>
<p>16. <strong>/ʒ/:</strong> Môi chu ra (giống khi yêu cầu ng khác im lặng:
Shhhhhh!). Nhưng có rung thanh quản, môi hướng về phía trước như đang
kiss ai đó, môi tròn, để mặt lưỡi chạm lợi hàm trên, nâng phần trước
của lưỡi lên.</p>
<p>17. <strong>/m/:</strong> Giống âm /m/ tiếng Việt, hai môi ngậm lại, để luồng khí
thoát qua mũi.</p>
<p>18. <strong>/n/:</strong> Khí thoát ra từ mũi, môi hé, đầu lưỡi chạm vào lợi hàm
trên, chặn luồng khí để khí thoát ra từ mũi.</p>
<p>19. <strong>/η/:</strong> Khí bị chặn ở lưỡi và ngạc mềm nên thoát ra từ mũi,
thanh quản rung, môi hé**,** phần sau của lưỡi nâng lên, chạm ngạc
